def toplama(x,y):
    """
    Funksiya toplama əməlini yerinə yetirir
    """
    result = x+y
    return result
def çıxma(x,y):
    """
    Funksiya çıxma əməlini yerinə yetirir
    """
    result = x-y 
    return result
def hasil(x,y):
    """
    Funksiya vurma əməlini yerinə yetirir
    """
    result = x*y 
    return result
def bölmə(x,y):
    """
    Funksiya bölmə əməlini yerinə yetirir
    """
    result = x/y 
    return result
